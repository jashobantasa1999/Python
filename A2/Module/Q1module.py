import math

def Add(a,b):
    return a+b

def Sub(a,b):
    return a-b

def multi(a,b):
    return a*b

def Div(a,b):
    return a//b

def mod(a,b):
    return a%b

def power(a,b):
    return math.pow(a,b)

def sqrt(a):
    return math.sqrt(a)

def cubicroot(a):
    return math.cbrt(a)

def Sin(a):
    return math.sin(a)

def cos(a):
    return math.cos(a)

def tan(a):
    return math.tan(a)

def log(a):
    return math.log(a)

def exp(a):
    return math.exp(a)



